    public static String toCamelCase(String input) {
        input = input.toLowerCase();
        StringBuilder titleCase = new StringBuilder();
        boolean nextTitleCase = true;

        for (char c : input.toCharArray()) {
            if (Character.isSpaceChar(c)) {
                nextTitleCase = true;
            } else if (nextTitleCase) {
                c = Character.toTitleCase(c);
                nextTitleCase = false;
            }

            titleCase.append(c);
        }

        return titleCase.toString();
    }

    public static String insertPeriodically(String text, String insert, int period) {
        StringBuilder builder = new StringBuilder(text.length() + insert.length() * (text.length() / period) + 1);
        int index = 0;
        String prefix = "";
        while (index < text.length()) {
            builder.append(prefix);
            prefix = insert;
            builder.append(text.substring(index, Math.min(index + period, text.length())));
            index += period;
        }
        return builder.toString();
    }

    public static String formatRupiah(int uang) {
        Locale localeID = new Locale("in", "ID");
        NumberFormat formatRupiah = NumberFormat.getCurrencyInstance(localeID);
        return formatRupiah.format(uang);
    }

    public static String formatRp(long uang) {
        String Rupiah = "Rp. ";

        Locale localeID = new Locale("in", "ID");
        NumberFormat rupiahFormat = NumberFormat.getInstance(localeID);
        String rupiah = rupiahFormat.format(uang);

        String Result = Rupiah + " " + rupiah;
        return Result;
    }

    @RequiresApi(api = Build.VERSION_CODES.N)
    private String toRupiah(String nominal) {
        String hasil = "";
        DecimalFormat toRupiah = (DecimalFormat) DecimalFormat.getCurrencyInstance();
        DecimalFormatSymbols formatAngka = new DecimalFormatSymbols();
        formatAngka.setCurrencySymbol("Rp. ");
        formatAngka.setMonetaryDecimalSeparator(',');
        formatAngka.setMonetaryGroupingSeparator('.');
        toRupiah.setDecimalFormatSymbols(formatAngka);
        hasil = toRupiah.format(Double.valueOf(nominal));
        return hasil;
    }

    public static void showAlert(final Context context, String msg) {
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.JELLY_BEAN_MR1) {
            new AlertDialog.Builder(context)
                    .setMessage(msg)
                    .setOnDismissListener(new DialogInterface.OnDismissListener() {
                        @Override
                        public void onDismiss(DialogInterface dialogInterface) {
                            dialogInterface.dismiss();
                            ((AppCompatActivity) context).finish();
                        }
                    }).create().show();
        }
    }

    public static void showAlert(final Context context, String msg, final OnDismissAlert onDismissAlert) {
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.JELLY_BEAN_MR1) {
            new AlertDialog.Builder(context)
                    .setMessage(msg)
                    .setOnDismissListener(dialogInterface -> onDismissAlert.dismiss(dialogInterface)).create().show();
        }
    }

    interface OnDismissAlert {
        void dismiss(DialogInterface dialogInterface);
    }

    public static <T> List<T> convertJsonArrayToList(String response) {
        Type collectionType = new TypeToken<List<T>>() {
        }.getType();
        return new Gson().fromJson(response, collectionType);
    }

    public static void looperHandler(final OnHandler handler) {
        new Handler(Looper.getMainLooper()).post(new Runnable() {
            @Override
            public void run() {
                try {
                    handler.run();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        });
    }

    interface OnHandler {
        void run();
    }

    public static String hmsTimeFormatter(long milliSeconds) {
        String hms = String.format("%02d:%02d:%02d",
                TimeUnit.MILLISECONDS.toHours(milliSeconds),
                TimeUnit.MILLISECONDS.toMinutes(milliSeconds) - TimeUnit.HOURS.toMinutes(TimeUnit.MILLISECONDS.toHours(milliSeconds)),
                TimeUnit.MILLISECONDS.toSeconds(milliSeconds) - TimeUnit.MINUTES.toSeconds(TimeUnit.MILLISECONDS.toMinutes(milliSeconds)));
        return hms;
    }
